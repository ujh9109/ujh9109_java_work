package test.dto;

public class MyDto {
	
	private int num;
	private String name;
	private String position;
	private String team;
	private String isWorldClass;
	
	public MyDto() {}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public String getIsWorldClass() {
		return isWorldClass;
	}

	public void setIsWorldClass(String isWorldClass) {
		this.isWorldClass = isWorldClass;
	}
	
	
	
	
}
